    /**
     * 判断标题是否为汇汇（忽略大小写）
     */
    public static boolean hui(String title) {
        return title != null && title.equalsIgnoreCase(TARGET);
    }

    /**
     * 判断标题是否不是汇汇（忽略大小写）
     */
    public static boolean not(String title) {
        return !hui(title);
    }

    /**
     * 判断是否为周末
     */
    public static boolean isWeekend(LocalDate date) {
        return date.getDayOfWeek() == DayOfWeek.SATURDAY || date.getDayOfWeek() == DayOfWeek.SUNDAY;
    }

    /**
     * 判断是否为休息日（周末 + 法定节假日）
     */
    public static boolean isRestDay(LocalDate date) {
        if (isWeekend(date)) {
            return true;
        }
        int m = date.getMonthValue();
        int d = date.getDayOfMonth();
        return (m == 1 && d >= 1 && d <= 3)
                || (m == 5 && d >= 1 && d <= 5)
                || (m == 10 && d >= 1 && d <= 7)
                || (m == 4 && d == 5)
                || (m == 6 && (d == 10 || d == 11 || d == 12))
                || (m == 9 && (d == 15 || d == 16 || d == 17))
                || (m == 2 && (d == 12 || d == 13 || d == 14 || d == 15));
    }

    /**
     * 随机获取一条温暖的假日问候
     * 周末：从通用 + 周末专属中随机选取
     * 法定假日（非周末）：只从通用列表中选取
     */
    public static String randomGreeting() {
        if (isWeekend(LocalDate.now())) {
            List<String> combined = new ArrayList<>(GENERAL_GREETINGS);
            combined.addAll(WEEKEND_GREETINGS);
            return combined.get(RNG.nextInt(combined.size()));
        }
        return GENERAL_GREETINGS.get(RNG.nextInt(GENERAL_GREETINGS.size()));
    }

    /**
     * 判断是否应该显示假日问候（汇汇 + 休息日）
     */
    public static boolean showGreeting(String title) {
        return hui(title) && isRestDay(LocalDate.now());
    }

    /**
     * 判断是否不显示假日问候（非汇汇 或 非休息日）
     */
    public static boolean noneGreeting(String title) {
        return !showGreeting(title);
    }
